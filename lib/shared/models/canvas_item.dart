class OverflowSegment {
  final List<Map<String, dynamic>>? ops; // text piece (null for tables)
  final TableData? tableData;             // table piece: header + slice of rows
  final double y;                         // absolute Y where it renders
  final double height;                    // engine-computed height
  const OverflowSegment({
    this.ops,
    this.tableData,
    required this.y,
    required this.height,
  });
}